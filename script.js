const keys = Object.keys(foodData).sort();
const searchBox = document.getElementById("searchBox");
const suggestions = document.getElementById("suggestions");
const result = document.getElementById("result");

searchBox.addEventListener("focus",()=>{ suggestions.style.display="block"; render(keys); });
searchBox.addEventListener("input",()=>{
 const q=searchBox.value.toLowerCase();
 render(keys.filter(k=>k.toLowerCase().includes(q)));
});
searchBox.addEventListener("keydown",e=>{ if(e.key==="Enter"){e.preventDefault(); search();}});
document.getElementById("searchBtn").onclick=search;

function render(list){
 suggestions.innerHTML="";
 list.forEach(k=>{
  const li=document.createElement("li");
  li.textContent=k;
  li.onclick=()=>select(k);
  suggestions.appendChild(li);
 });
}

function select(k){
 searchBox.value=k;
 suggestions.style.display="none";
 show(k);
}

function search(){
 const q=searchBox.value.toLowerCase();
 const found=keys.find(k=>k.toLowerCase().includes(q));
 if(found) show(found);
 else result.innerHTML="<b>Food not found</b>";
}

