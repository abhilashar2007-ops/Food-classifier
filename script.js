let foodKeys = Object.keys(foodData);

function showList(){
  const ul = document.getElementById("foodList");
  ul.innerHTML="";
  foodKeys.forEach(k=>{
    let li=document.createElement("li");
    li.textContent=k;
    li.onclick=()=>selectFood(k);
    ul.appendChild(li);
  });
}

function filterList(){
  const q=document.getElementById("searchBox").value.toLowerCase();
  const ul=document.getElementById("foodList");
  ul.innerHTML="";
  foodKeys.filter(k=>k.includes(q)).forEach(k=>{
    let li=document.createElement("li");
    li.textContent=k;
    li.onclick=()=>selectFood(k);
    ul.appendChild(li);
  });
}

function selectFood(key){
  document.getElementById("searchBox").value=key;
  document.getElementById("foodList").innerHTML="";
  showFood(key);
}

