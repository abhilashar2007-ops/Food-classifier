const keys = Object.keys(foodData);
const searchBox = document.getElementById("searchBox");
const suggestions = document.getElementById("suggestions");
const result = document.getElementById("result");

searchBox.addEventListener("focus",()=>{
  suggestions.style.display="block";
  renderList(keys);
});

searchBox.addEventListener("input",()=>{
  const q=searchBox.value.toLowerCase();
  const filtered=keys.filter(k=>k.includes(q));
  renderList(filtered);
});

searchBox.addEventListener("keydown",e=>{
  if(e.key==="Enter"){
    e.preventDefault();
    searchFood();
  }
});

document.getElementById("searchBtn").onclick=searchFood;

function renderList(list){
  suggestions.innerHTML="";
  list.forEach(k=>{
    const li=document.createElement("li");
    li.textContent=k;
    li.onclick=()=>selectFood(k);
    suggestions.appendChild(li);
  });
}

function selectFood(key){
  searchBox.value=key;
  suggestions.style.display="none";
  showFood(key);
}

function searchFood(){
  const q=searchBox.value.toLowerCase();
  const found=keys.find(k=>k.includes(q));
  if(found) showFood(found);
  else result.innerHTML="<b>Food not found</b>";
}

function showFood(key){
  const f=foodData[key];
  result.innerHTML=`
    <h2>${key}</h2>
    <span class="badge ${f.category}">${f.category}</span>
    <div class="grid">
      <div>
        <b>Calories:</b> ${f.nutrients.calories} kcal<br>
        <b>Protein:</b> ${f.nutrients.protein} g<br>
        <b>Carbs:</b> ${f.nutrients.carbs} g<br>
        <b>Fat:</b> ${f.nutrients.fat} g<br>
        <b>Fiber:</b> ${f.nutrients.fiber} g<br>
        <b>Sugar:</b> ${f.nutrients.sugar} g
      </div>
      <div>
        <b>Best time:</b> ${f.bestTime}<br>
        <b>Quantity:</b> ${f.quantity}<br><br>
        <b>Advantages:</b><ul>${f.advantages.map(a=>`<li>${a}</li>`).join("")}</ul>
        <b>Disadvantages:</b><ul>${f.disadvantages.map(d=>`<li>${d}</li>`).join("")}</ul>
      </div>
    </div>
  `;
}
